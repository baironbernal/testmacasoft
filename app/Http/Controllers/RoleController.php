    {
        $rules = [
            'name' => 'required|string',
        ];

        $validator = Validator::make($request->all(), $rules);

        if ($validator->fails()) {
            return Redirect::back()
                        ->withErrors($validator)
                        ->withInput();
        } 
        else {
            $role = Role::create(['name' => $request->name,'guard_name' => 'web']);

            return back()->with('status', 'Role creado');
        }
